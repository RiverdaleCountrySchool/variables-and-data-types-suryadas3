public class IntegerCalc {
    public static void main(String[] args) {
        // Declare variables num1 and num2 and assign them
        // initial values of 11 and 5.
        int num1 = 11;
        int num2 = 5;

        System.out.println (num1 + num2);
        System.out.println (num1 - num2);
        System.out.println (num1 * num2);
        System.out.println (num1 / num2);


    }
}
